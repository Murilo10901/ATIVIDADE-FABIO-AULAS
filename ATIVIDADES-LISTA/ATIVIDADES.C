#include <stdio.h>

int main() {
    int n, soma = 0;

    printf("Digite um número inteiro positivo: ");
    scanf("%d", &n);

    for (int i = 1; i <= n; i += 2) {
        soma += i;
    }

    printf("A soma dos números ímpares de 1 até %d é: %d\n", n, soma);
    return 0;
}
